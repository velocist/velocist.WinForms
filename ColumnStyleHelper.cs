namespace velocist.WinForms {

	/// <summary>
	/// Properties styles for column of winforms datatables
	/// </summary>
	public class ColumnStyleHelper {

		/// <summary>
		/// Gets or sets a value indicating whether this <see cref="ColumnStyleHelper"/> is key.
		/// </summary>
		/// <value>
		///   <c>true</c> if key; otherwise, <c>false</c>.
		/// </value>
		public bool Key { get; set; }

		/// <summary>
		/// Gets or sets the codigo.
		/// </summary>
		/// <value>
		/// The codigo.
		/// </value>
		public object Code { get; set; }

		/// <summary>
		/// Gets or sets the text.
		/// </summary>
		/// <value>
		/// The text.
		/// </value>
		public object Text { get; set; }

		/// <summary>
		/// Gets or sets the link.
		/// </summary>
		/// <value>
		/// The link.
		/// </value>
		public string Link { get; set; }

		/// <summary>
		/// Gets or sets the format.
		/// </summary>
		/// <value>
		/// The format.
		/// </value>
		public string Format { get; set; }

		/// <summary>
		/// Gets or sets a value indicating whether this <see cref="ColumnStyleHelper"/> is bold.
		/// </summary>
		/// <value>
		///   <c>true</c> if bold; otherwise, <c>false</c>.
		/// </value>
		public bool Bold { get; set; }

		/// <summary>
		/// Gets or sets a value indicating whether this <see cref="ColumnStyleHelper"/> is hide.
		/// </summary>
		/// <value>
		///   <c>true</c> if hide; otherwise, <c>false</c>.
		/// </value>
		public bool Hide { get; set; }

		/// <summary>
		/// Gets or sets the color of the text.
		/// </summary>
		/// <value>
		/// The color of the text.
		/// </value>
		public string TextColor { get; set; }

		/// <summary>
		/// Gets or sets the text align.
		/// </summary>
		/// <value>
		/// The text align.
		/// </value>
		public string TextAlign { get; set; }

		/// <summary>
		/// Gets or sets the color.
		/// </summary>
		/// <value>
		/// The color.
		/// </value>
		public string Color { get; set; }

		/// <summary>
		/// Gets or sets the size.
		/// </summary>
		/// <value>
		/// The size.
		/// </value>
		public string Size { get; set; }
	}
}
